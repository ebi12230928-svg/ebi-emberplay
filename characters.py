def _custom_characters_raw():
    """管理者が作成したキャラクターを、静的キャラクターと同じタプル形式に変換して返す"""
    from models import CustomCharacter
    import json as _json

    result = {}
    for row in CustomCharacter.query.all():
        result[row.key] = (
            row.name, row.rarity, row.element, row.icon, row.attack, row.range, row.speed,
            row.cost, row.splash, row.description or "",
        )
    return result


def _custom_extra_fields():
    """管理者作成キャラクターの、defense・abilitiesなど拡張フィールドだけを別途取得する"""
    from models import CustomCharacter
    import json as _json

    result = {}
    for row in CustomCharacter.query.all():
        try:
            abilities = _json.loads(row.abilities_json)
        except (ValueError, TypeError):
            abilities = []
        result[row.key] = {"defense": row.defense, "abilities": abilities}
    return result


