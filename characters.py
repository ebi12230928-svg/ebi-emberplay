def character_info(key):
    """辞書形式でキャラクター情報を返す(テンプレート・JSONで扱いやすくするため)。defense・abilitiesも含む"""
    all_chars = all_characters_dict()
    if key not in all_chars:
        return None
    name, rarity, element, icon, atk, rng, speed, cost, splash, desc = all_chars[key]

    defense = 10.0
    abilities = []
    if key in CHARACTERS:
        abilities = STATIC_ABILITIES.get(key, [])
    else:  # 管理者作成キャラクターの場合は拡張フィールドを取得する
        extra = _custom_extra_fields().get(key)
        if extra:
            defense = extra["defense"]
            abilities = extra["abilities"]

    return {
        "key": key, "name": name, "rarity": rarity, "rarity_label": RARITY_NAMES[rarity],
        "element": element, "element_label": ELEMENT_NAMES[element], "icon": icon,
        "attack": atk, "defense": defense, "range": rng, "speed": speed, "cost": cost, "splash": splash,
        "abilities": abilities, "description": desc, "colors": RARITY_COLORS[rarity],
    }


def stats_at_level(key, level):
    """重複取得によるレベルアップで、攻撃力が緩やかに強化される(レベルごとに+8%)"""
    info = character_info(key)
    if not info:
        return None
    multiplier = 1 + (level - 1) * 0.08
    info = dict(info)
    info["level"] = level
    info["attack"] = round(info["attack"] * multiplier, 1)
    info["defense"] = round(info["defense"] * multiplier, 1)
    return info
